
public class Spikey {
	public static void main(String[] args) {
		oneSlash();
		twoSlash();
		threeSlash();
		slashThree();
		slashTwo();
		slashOne();
	}
	
	public static void oneSlash() {
		System.out.println("  \\/  ");
	}
	
	public static void twoSlash() {
		System.out.println(" \\\\// ");
	}
	
	public static void threeSlash() {
		System.out.println("\\\\\\///");
	}
	
	public static void slashOne() {
		System.out.println("  /\\  ");
	}
	
	public static void slashTwo() {
		System.out.println(" //\\\\ ");
	}
	
	public static void slashThree() {
		System.out.println("///\\\\\\");
	}
}
